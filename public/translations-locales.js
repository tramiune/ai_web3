function deepMergeI18n(target, source) {
    if (!source || typeof source !== 'object') return target;
    for (const key of Object.keys(source)) {
        const val = source[key];
        if (val && typeof val === 'object' && !Array.isArray(val)) {
            if (!target[key] || typeof target[key] !== 'object') target[key] = {};
            deepMergeI18n(target[key], val);
        } else {
            target[key] = val;
        }
    }
    return target;
}

if (window.TRANSLATIONS?.en && window.TRANSLATIONS_LOCALES) {
    for (const [code, overrides] of Object.entries(window.TRANSLATIONS_LOCALES)) {
        window.TRANSLATIONS[code] = deepMergeI18n(
            JSON.parse(JSON.stringify(window.TRANSLATIONS.en)),
            overrides
        );
    }
}
